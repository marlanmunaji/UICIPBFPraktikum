var tanggal = 29; 
// mendeklarasikan variabel tanggal dengan nilai awal angka 29 (tipe number)

tanggal = "30"; 
// mengubah nilai tanggal menjadi string "30" (JavaScript bersifat dinamis, tipe data bisa berubah)

let namaBulan = "September"; 
// mendeklarasikan variabel namaBulan dengan nilai string "September"

namaBulan = 10; 
// mengubah nilai namaBulan menjadi angka 10 (tipe data berubah dari string ke number)

const tahun = "2022"; 
// mendeklarasikan konstanta tahun, nilainya tidak bisa diubah

alert(tanggal + " " + namaBulan + " " + tahun); 
// menggabungkan nilai tanggal, namaBulan, dan tahun menjadi satu string lalu menampilkannya